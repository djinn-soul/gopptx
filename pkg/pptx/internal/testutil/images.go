package testutil

// TinyPNG returns a tiny 1x1 transparent PNG.
//
// These bytes come from image/png's own encoder and decode cleanly. An earlier
// hand-written version had a bad IDAT checksum, so every renderer rejected it;
// that went unnoticed while the PDF exporter silently dropped unusable images.
